def definir_rois(animals_comuns, arquivos_topscan):
    """
    Builds the ROI dictionary by animal from ROI_TEMPLATES_DINAMICO.
    The template key is the video name (e.g., 'RR.mp4'), obtained from arquivos_topscan.
    Injects 'tipo': 'circulo' into each ROI for compatibility with video/exploration functions.
    """
    rois = {}
    for animal_id in sorted(animals_comuns):
        info = arquivos_topscan.get(animal_id)
        if not info:
            print(f"  WARNING: '{animal_id}' has no entry in arquivos_topscan.")
            continue
        video_nome = info.get("video_cortado_nome", "")
        if video_nome in ROI_TEMPLATES_DINAMICO:
            rois[animal_id] = {
                nome_roi: {**coords, 'tipo': 'circulo'}
                for nome_roi, coords in ROI_TEMPLATES_DINAMICO[video_nome].items()
            }
            print(f"  {animal_id} -> {video_nome}")
        else:
            print(f"  ERROR: '{video_nome}' not found in ROI_TEMPLATES_DINAMICO.")
    return rois


def verificar_rois(rois_definidas, arquivos_topscan):
    print("\n--- Visual ROI check ---")
    import random
    for animal_id, rois in rois_definidas.items():
        info = arquivos_topscan.get(animal_id)
        if not info:
            continue
        caminho_video = os.path.join(DIRETORIO_VIDEOS, info["video_cortado_nome"])
        if not os.path.exists(caminho_video):
            print(f"  ERROR: video not found in '{caminho_video}'")
            continue

        cap = cv2.VideoCapture(caminho_video)
        if not cap.isOpened():
            continue
        total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
        cap.set(cv2.CAP_PROP_POS_FRAMES, random.randint(int(total * 0.1), int(total * 0.9)))
        ret, frame = cap.read()
        cap.release()
        if not ret:
            continue

        for nome, info_roi in rois.items():
            if info_roi.get('tipo') == 'circulo':
                cor = (255, 100, 100) if "Familiar" in nome else (100, 100, 255)
                cv2.circle(frame, (info_roi['x'], info_roi['y']), info_roi.get('radius', info_roi.get(LEGACY_RADIUS_KEY)), cor, 2)
                cv2.putText(frame, nome, (info_roi['x'] + info_roi.get('radius', info_roi.get(LEGACY_RADIUS_KEY)) + 5, info_roi['y'] + 5),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.7, (255, 255, 255), 2)

        plt.figure(figsize=(10, 8))
        plt.imshow(cv2.cvtColor(frame, cv2.COLOR_BGR2RGB))
        plt.title(f"ROI — {animal_id}")
        plt.axis('off')
        plt.show()


