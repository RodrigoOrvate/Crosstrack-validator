def _long_to_wide_two_groups(df_long, col_sujeito, col_group, col_valor, group_a, group_b):
    """Converts long→wide with two columns (group_a | group_b), aligned by subject."""
    g = df_long.groupby([col_sujeito, col_group])[col_valor].mean().unstack(col_group)
    g = g.reset_index().rename(columns={col_sujeito: 'Animal_ID'})
    for col in [group_a, group_b]:
        if col not in g.columns:
            g[col] = np.nan
    return g[['Animal_ID', group_a, group_b]]

