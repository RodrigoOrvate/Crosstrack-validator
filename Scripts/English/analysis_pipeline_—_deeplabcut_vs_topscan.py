def normalize_roi_template_keys(roi_templates):
    """Converts legacy ROI keys from Portuguese to English without breaking old JSON files."""
    if not isinstance(roi_templates, dict):
        return roi_templates
    for video_key, roi_dict in list(roi_templates.items()):
        if not isinstance(roi_dict, dict):
            continue

        # Legacy object label used by older Portuguese ROI dictionaries.
        legacy_novel_key = ''.join(['N', 'o', 'v', 'o'])
        if legacy_novel_key in roi_dict and 'Novel' not in roi_dict:
            roi_dict['Novel'] = roi_dict.pop(legacy_novel_key)

        for object_key, coords in list(roi_dict.items()):
            if isinstance(coords, dict) and LEGACY_RADIUS_KEY in coords and 'radius' not in coords:
                coords['radius'] = coords.pop(LEGACY_RADIUS_KEY)
    return roi_templates


def salvar_roi_templates_json(roi_templates=None, caminho=CAMINHO_ROI_TEMPLATES_JSON):
    """Saves current ROIs as JSON in Drive to avoid losing them after Colab disconnects."""
    if roi_templates is None:
        roi_templates = normalize_roi_template_keys(globals().get('ROI_TEMPLATES_DINAMICO', {}))
    os.makedirs(os.path.dirname(caminho), exist_ok=True)
    with open(caminho, 'w', encoding='utf-8') as f:
        json.dump(roi_templates, f, ensure_ascii=False, indent=2)
    print(f"✅ ROIs saved to: {caminho}")
    return caminho


def carregar_roi_templates_json(caminho=CAMINHO_ROI_TEMPLATES_JSON, fallback=None, salvar_fallback=True):
    """Loads ROIs from JSON; if the file does not exist, uses the fallback and optionally saves it."""
    if fallback is None:
        fallback = {}
    fallback = normalize_roi_template_keys(fallback)
    if os.path.exists(caminho):
        with open(caminho, 'r', encoding='utf-8') as f:
            dados = normalize_roi_template_keys(json.load(f))
        print(f"✅ ROIs loaded from JSON: {caminho} ({len(dados)} videos)")
        return dados
    print("ℹ️ ROI JSON not found yet. Using ROI_TEMPLATES_DINAMICO defined in the notebook.")
    if salvar_fallback and fallback:
        salvar_roi_templates_json(fallback, caminho)
    return normalize_roi_template_keys(fallback)


def resumir_controle_qualidade(resultados_cm, bodypart=BODYPART_REFERENCIA):
    """Shows an early QC table immediately after processing in cm."""
    rows = []
    for animal_id in sorted(resultados_cm.keys()):
        try:
            df_ts = resultados_cm[animal_id][bodypart]['topscan']
            df_dlc = resultados_cm[animal_id][bodypart]['dlc']
        except Exception:
            continue

        qc = 'ok'
        try:
            serie_qc = df_ts.get('qc_flag')
            if isinstance(serie_qc, pd.Series) and not serie_qc.empty:
                qc = str(serie_qc.iloc[0])
        except Exception:
            pass

        n_ts = len(df_ts) if isinstance(df_ts, pd.DataFrame) else 0
        n_dlc = len(df_dlc) if isinstance(df_dlc, pd.DataFrame) else 0
        rows.append({
            'Animal_ID': animal_id,
            'Bodypart': bodypart,
            'Frames_TopScan': n_ts,
            'Frames_DLC': n_dlc,
            'QC': qc,
            'Needs_attention': qc != 'ok',
            'Modo_conservador': animal_id in RATOS_MODO_CONSERVADOR,
        })

    df_qc = pd.DataFrame(rows)
    if df_qc.empty:
        print("⚠️ No results available for QC summary.")
        return df_qc

    print("\n=== Early Quality-Control Summary (QC) ===")
    display(df_qc)

    problemas = df_qc[df_qc['Needs_attention']]
    if problemas.empty:
        print("✅ No animal was flagged with problematic QC.")
    else:
        print("⚠️ Animals that require checking before statistics:")
        display(problemas)
    return df_qc

class CalibradorSerial:
    def __init__(self, lista_videos):
        self.lista = lista_videos
        self.index = 0
        self.frame_atual = None
        self.nome_atual = ""
        self.setup_widgets()
        self.carregar_video_atual()

    def setup_widgets(self):
        self.style = {'description_width': 'initial'}
        # Sliders Familiar
        self.fam_x = widgets.IntSlider(value=118, min=0, max=400, description='Fam X:', style=self.style)
        self.fam_y = widgets.IntSlider(value=112, min=0, max=400, description='Fam Y:', style=self.style)
        self.fam_r = widgets.IntSlider(value=17, min=5, max=50, description='Fam. radius:', style=self.style)
        # Novel-object sliders
        self.nov_x = widgets.IntSlider(value=208, min=0, max=400, description='Novel X:', style=self.style)
        self.nov_y = widgets.IntSlider(value=110, min=0, max=400, description='Novel Y:', style=self.style)
        self.nov_r = widgets.IntSlider(value=17, min=5, max=50, description='Novel radius:', style=self.style)

        self.btn_proximo = widgets.Button(description="Save and Next", button_style='success', icon='arrow-right')
        self.btn_proximo.on_click(self.proximo_video)

        self.output_plot = widgets.Output()
        self.txt_status = widgets.HTML(value="<b>Starting calibration...</b>")

    def carregar_video_atual(self):
        if self.index < len(self.lista):
            self.nome_atual = self.lista[self.index]
            path = os.path.join(DIRETORIO_VIDEOS, self.nome_atual)
            cap = cv2.VideoCapture(path)
            ret, frame = cap.read()
            cap.release()

            if ret:
                self.frame_atual = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
                h, w, _ = self.frame_atual.shape
                # Updates slider limits according to video resolution
                self.fam_x.max, self.nov_x.max = w, w
                self.fam_y.max, self.nov_y.max = h, h
                self.txt_status.value = f"<b>Video ({self.index+1}/{len(self.lista)}):</b> {self.nome_atual} | Res: {w}x{h}"
                self.render()
        else:
            self.finalizar()

    def render(self, *args):
        with self.output_plot:
            clear_output(wait=True)
            img = self.frame_atual.copy()
            # Desenha ROIs
            cv2.circle(img, (self.fam_x.value, self.fam_y.value), self.fam_r.value, (0, 255, 0), 2)
            cv2.circle(img, (self.nov_x.value, self.nov_y.value), self.nov_r.value, (255, 0, 0), 2)

            plt.figure(figsize=(8, 5))
            plt.imshow(img)
            plt.axis('off')
            plt.show()

    def proximo_video(self, b):
        # Saves to the global variable
        ROI_TEMPLATES_DINAMICO[self.nome_atual] = {
            "Familiar": {'x': self.fam_x.value, 'y': self.fam_y.value, 'radius': self.fam_r.value},
            "Novel": {'x': self.nov_x.value, 'y': self.nov_y.value, 'radius': self.nov_r.value}
        }
        print(f"✅ Saved {self.nome_atual}: {ROI_TEMPLATES_DINAMICO[self.nome_atual]}")
        salvar_roi_templates_json(ROI_TEMPLATES_DINAMICO)

        self.index += 1
        self.carregar_video_atual()

    def finalizar(self):
        clear_output()
        print("\n" + "="*40)
        print("🎉 CALIBRATION COMPLETE!")
        print(f"Total de videos processados: {len(ROI_TEMPLATES_DINAMICO)}")
        salvar_roi_templates_json(ROI_TEMPLATES_DINAMICO)
        print("The variable 'ROI_TEMPLATES_DINAMICO' is ready for the pipeline and has been saved as JSON.")
        print("="*40)

    def iniciar(self):
        # Layout
        ui = widgets.VBox([
            self.txt_status,
            widgets.HBox([self.fam_x, self.fam_y, self.fam_r]),
            widgets.HBox([self.nov_x, self.nov_y, self.nov_r]),
            self.btn_proximo,
            self.output_plot
        ])
        # Link sliders to rendering
        for w in [self.fam_x, self.fam_y, self.fam_r, self.nov_x, self.nov_y, self.nov_r]:
            w.observe(self.render, names='value')

        display(ui)


