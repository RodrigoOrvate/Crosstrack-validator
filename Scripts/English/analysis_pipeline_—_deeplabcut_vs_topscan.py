def exportar_concordancia_body_csv(
    animals_comuns,
    resultados_cm,
    diretorio_saida,
    bodypart='body'
):
    """
    Exports frame-by-frame agreement metrics using the body.
    The body is used for kinematic agreement, while the nose is reserved
    for object-exploration classification.
    """

    os.makedirs(diretorio_saida, exist_ok=True)

    rows_por_animal = []
    rows_frame = []

    for animal_id in sorted(animals_comuns):
        df_an = _sincronizar_dados_cm(
            animal_id,
            resultados_cm,
            bodypart=bodypart
        )

        if df_an is None or df_an.empty:
            continue

        df_an = df_an.copy()

        # Ensures the time column exists
        if 'time' not in df_an.columns:
            if 'time_topscan' in df_an.columns:
                df_an['time'] = df_an['time_topscan']
            elif 'time_dlc' in df_an.columns:
                df_an['time'] = df_an['time_dlc']
            else:
                df_an['time'] = df_an['FrameNum'] / FPS_VIDEO

        df_an['error_body_cm'] = np.sqrt(
            (df_an['x_cm_dlc'] - df_an['x_cm_topscan'])**2 +
            (df_an['y_cm_dlc'] - df_an['y_cm_topscan'])**2
        )

        erro = df_an['error_body_cm'].dropna()

        if erro.empty:
            continue

        rows_por_animal.append({
            'ID': animal_id,
            'Bodypart': bodypart,
            'N_frames': int(erro.shape[0]),
            'Mean_error_body_cm': erro.mean(),
            'Median_error_body_cm': erro.median(),
            'SD_error_body_cm': erro.std(),
            'Min_error_body_cm': erro.min(),
            'P75_error_body_cm': erro.quantile(0.75),
            'P90_error_body_cm': erro.quantile(0.90),
            'P95_error_body_cm': erro.quantile(0.95),
            'P99_error_body_cm': erro.quantile(0.99),
            'Max_error_body_cm': erro.max(),
            'Frames_error_up_to_2cm_pct': (erro <= 2).mean() * 100,
            'Frames_error_up_to_5cm_pct': (erro <= 5).mean() * 100,
            'Frames_error_above_10cm_pct': (erro > 10).mean() * 100,
        })

        columns_frame = [
            'FrameNum',
            'time',
            'x_cm_topscan',
            'y_cm_topscan',
            'x_cm_dlc',
            'y_cm_dlc',
            'error_body_cm'
        ]

        columns_existentes = [c for c in columns_frame if c in df_an.columns]

        df_frame = df_an[columns_existentes].copy()
        df_frame.insert(0, 'ID', animal_id)
        df_frame.insert(1, 'Bodypart', bodypart)

        rows_frame.append(df_frame)

    df_body_animal = pd.DataFrame(rows_por_animal)

    if rows_frame:
        df_body_frame = pd.concat(rows_frame, ignore_index=True)
    else:
        df_body_frame = pd.DataFrame()

    if not df_body_animal.empty:
        resumo_group = pd.DataFrame([{
            'Bodypart': bodypart,
            'N_animals': df_body_animal['ID'].nunique(),
            'Group_mean_error_cm': df_body_animal['Mean_error_body_cm'].mean(),
            'Group_mean_error_SD_cm': df_body_animal['Mean_error_body_cm'].std(),
            'Group_median_error_cm': df_body_animal['Median_error_body_cm'].median(),
            'Group_P95_error_cm': df_body_animal['P95_error_body_cm'].mean(),
            'Mean_pct_frames_error_up_to_2cm': df_body_animal['Frames_error_up_to_2cm_pct'].mean(),
            'Mean_pct_frames_error_up_to_5cm': df_body_animal['Frames_error_up_to_5cm_pct'].mean(),
            'Mean_pct_frames_error_above_10cm': df_body_animal['Frames_error_above_10cm_pct'].mean(),
        }])
    else:
        resumo_group = pd.DataFrame()

    path_animal = os.path.join(diretorio_saida, 'agreement_body_by_animal.csv')
    path_frame = os.path.join(diretorio_saida, 'agreement_body_frame_by_frame.csv')
    path_resumo = os.path.join(diretorio_saida, 'agreement_body_group_summary.csv')

    df_body_animal.to_csv(path_animal, index=False, float_format='%.6f')
    df_body_frame.to_csv(path_frame, index=False, float_format='%.6f')
    resumo_group.to_csv(path_resumo, index=False, float_format='%.6f')

    print(f"✅ Agreement BODY by animal → {path_animal}")
    print(f"✅ Agreement BODY frame by frame → {path_frame}")
    print(f"✅ Agreement BODY group summary → {path_resumo}")

    return df_body_animal, df_body_frame, resumo_group


